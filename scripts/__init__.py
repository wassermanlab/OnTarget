class OnTargetUtilities:
    """
    Contains functions designed to work through the entire module.
    """

    #--------------#
    # Defaults     #
    #--------------#

    def __init__(self):
        """
        OnTarget options:
        """

        # GUD (hg38) RESTful API
        self.gud = "http://gud.cmmt.ubc.ca:8080/api/v1/hg38"

        # i.e. defaults for sample2gene.py
        self._max_tss = 100
        self._min_percent = 0.0
        self._min_tpm  = 100.0

    @property
    def max_tss(self):
        """
        Max. number of TSSs to return:
        @rtype = {Int}
        """
        return(self._max_tss)

    @property
    def min_percent(self):
        """
        Min. percentile of expression for TSS in samples:
        @rtype = {Float}
        """
        return(self._min_percent)

    @property
    def min_tpm(self):
        """
        Min. expression levels (in TPM) for TSS in samples:
        @rtype = {Float}
        """
        return(self._min_tpm)

